import mongoose from 'mongoose';

const ScholarshipSchema = new mongoose.Schema({
  className: {
    type: String,
    required: true,
  },
  studentId: {
    type: String,
    required: true,
  },
  studentName: {
    type: String,
    required: true,
  },
  benefactorId: {
    type: String,
    required: true,
  },
  benefactorName: {
    type: String,
    required: true,
  },
  amount: {
    type: Number,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  isActive: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.models.Scholarship || mongoose.model('Scholarship', ScholarshipSchema);
